public class BuclesAnidados {
    public static void main(String[] args) throws Exception {
        int M;

        for (int I = 1; I <= 10; I++) {
            for (int J = 1; J <= 10; J++) {
                M = I * J;
                System.out.println(I + " * " + J + " = " + M);

            }

            System.out.println();
        }

    }
}
